/* 

OPERADORES RELACIONAIS:
=   >> Atribuição
==  >> Igual
>   >> Maior
>=  >> Maior ou Igual
<   >> Menor
<=  >> Menor ou Igual
!=  >> Diferente

*/

let n1 = 10
let n2 = 5
let n3 = 10

console.log(n1 > n2)
console.log(n1 < n2)
console.log(n1 >= n3)
console.log(n1 <= n3)
console.log(n1 == n3)
console.log(n1 != n3)