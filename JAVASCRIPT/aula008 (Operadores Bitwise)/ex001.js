let n1 = 10
let n2 = 11

// OPERADORES LÓGICOS (BITWISE)
let res = n1 & n2
let res = n1 | n2
let res = n1 ^ n2

// DESLOGAMENTO DE BITS
let res = n1 << 1 // O código binário é deslocado em uma unidade para a esquerda e dobra o seu valor

let res = n1 >> 1 // O código binário é deslocado em uma unidade para a direita e divide seu valor na metade

console.log(res)